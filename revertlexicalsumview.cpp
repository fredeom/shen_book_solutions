#include <iostream>

using namespace std;

void draw(int k, int *x) {
  for (int i = 0; i < k; ++i) {
    if (i) cout << "+";
    cout << x[i];
  }
  cout << endl;
}

int main() {
  int n, *x, k, i, sum;
  cin >> n;
  x = new int[n];
  x[0] = n;

  draw(1, x);

  k = 1;
  while (k < n) {
    if (k == 1) {
      x[1] = x[0] / 2 + (x[0] % 2);
      x[0] = x[0] / 2;
      k = 2;
    } else if (2*x[k-2] <= x[k-1]) {
      x[k] = x[k-1] / 2 + (x[k-1] % 2);
      x[k-1] = x[k-1] / 2;
      k++;
    } else if (k > 2 && x[k-2] > x[k - 3]) {
      x[k-1]++;
      x[k-2]--;
    } else {
      i = k - 2; sum = x[k-1];
      while (i > 0 && x[i-1] == x[i]) {sum += x[i]; i--;}
      x[i+1] = sum + 1;
      x[i]--;
      k = i + 2;
    }
    draw(k, x);
  }
  return 0;
}


